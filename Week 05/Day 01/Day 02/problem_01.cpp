#include <bits/stdc++.h>
#define nl '\n'
#define nel cout<<nl
using namespace std;
int main(){
    ios::sync_with_stdio(false);
    cin.tie(NULL);
    int t;
    cin>>t;
    while (t--)
    {
        int n;
        cin>>n;
        for (int i = n; i > 0; i--)
        {
            cout<<i<<" ";
        }
        nel;
    }
    
    return 0;
}